import java.util.Scanner;

public class ScannerUsage {

    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        //read a line
        sc.nextLine();
        //read a word
        sc.next();
        //read an int
        sc.nextInt();
        //read a double
        sc.nextDouble();
        //read a boolean
        sc.nextBoolean();


    }
}
